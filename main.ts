let PlayerState = 0
let PlayerController: Sprite = null
let PlayerStateName = ""
let Debug: TextSprite = null
let acceleration = 25
let friction = 23
init()
Debug = textsprite.create(PlayerStateName, 1, 12)
game.onUpdate(function () {
    if (PlayerState == 1) {
        PlayerController.vx += -1 * acceleration
    } else if (PlayerState == 2) {
        PlayerController.vx += acceleration
    }
})
game.onUpdate(function () {
    if (controller.left.isPressed() && !(controller.right.isPressed())) {
        PlayerState = 1
    } else if (controller.right.isPressed() && !(controller.left.isPressed())) {
        PlayerState = 2
    } else {
        PlayerState = 0
    }
    PlayerController.vx = (100 - friction) / 100 * PlayerController.vx
    PlayerController.ay = 9.81 * 16
})
game.onUpdate(function () {
    if (PlayerState == 0) {
        PlayerStateName = "Idle"
    } else if (PlayerState == 1) {
        PlayerStateName = "Moving Left"
    } else if (PlayerState == 2) {
        PlayerStateName = "Moving Right"
    }
})
